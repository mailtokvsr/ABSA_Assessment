package org.venkat.api.pojo;


public class TestCasesData {
      private String testCasesName;
      private String executionFlag;
      private String className;


      public String getTestCasesName() {
            return testCasesName;
      }

      public void setTestCasesName(String testCasesName) {
            this.testCasesName = testCasesName;
      }

      public String getExecutionFlag() {
            return executionFlag;
      }

      public void setExecutionFlag(String executionFlag) {
            this.executionFlag = executionFlag;
      }

      public String getClassName() {
            return className;
      }

      public void setClassName(String className) {
            this.className = className;
      }

}
